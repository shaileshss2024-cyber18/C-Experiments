#include <stdio.h>

int main() {
    int age = 20;
    float height = 5.9f;
    double salary = 45678.75;
    char grade = 'A';

    printf("Integer value (age): %d\n", age);
    printf("Float value (height): %f\n", height);
    printf("Double value (salary): %lf\n", salary);
    printf("Character value (grade): %c\n", grade);

    return 0;
}
